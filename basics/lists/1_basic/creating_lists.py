# Lists can hold multiple items in a single variable.
# Creating lists using []
# ordered,mutable,allow duplicates
x=[1,2,3,4]                 # using []
y=list(["a","b","c"])        # using list() constructor
z=[1,"a",True,1.5,(1,2,3)]   # can have diff datatypes
print(f"{x} typeof x is {type(x)}")
print(f"{y} type y  is {type(y)}")
print(f"{z} type of z is {type(z)}")
