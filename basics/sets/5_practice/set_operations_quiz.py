A = {1, 2, 3}
B = {2, 3, 4}

print("A | B =", A | B)                 # union     both ele
print("A & B =", A & B)                 # intersection  comman ele
print("A - B =", A - B)                 # difference    ele unique in first set
print("A ^ B =", A ^ B)                 # symmetric_differnce   diff ele in both sets
