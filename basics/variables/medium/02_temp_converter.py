# Celsius to Fahrenheit

celsius = 37
# To convert Celsius to Fahrenheit, multiply the Celsius temperature by 9/5 and then add 32.
# The formula:F = (C * 9/5) + 32
fahrenheit=(celsius* 9/5)+32
print(f"Celsius to Fahrenheit:{(celsius* 9/5)+32}")
print(f"{celsius}째C = {fahrenheit}째F")


